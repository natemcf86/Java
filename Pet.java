
public class Pet {
	//global variables
	private String name;
	private String type;
	private int age;

	public Pet() {
		
	}
//getters and setters
	public String getName() {
		return name;
	}
	public void setName(String n) {
		this.name = n;
	}
	public String getType() {
		return type;
	}
	public void setType(String t) {
		this.type = t;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int a) {
		this.age = a;
	}
}
